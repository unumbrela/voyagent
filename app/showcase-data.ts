/** 显示用花费字符串 → 数字（存库/预算用）。"¥688/晚"→688，"免费预约"→0，null→0 */
export function costToNumber(cost: string | null | undefined): number {
  if (!cost) return 0;
  const m = cost.replace(/,/g, "").match(/-?\d+(\.\d+)?/);
  return m ? Number(m[0]) : 0;
}

/** 两点球面距离（km），用于「今日动线」统计（真实 WGS-84） */
export function haversineKm(a: Stop, b: Stop): number {
  const R = 6371;
  const rad = (x: number) => (x * Math.PI) / 180;
  const dLat = rad(b.lat - a.lat);
  const dLon = rad(b.lon - a.lon);
  const s =
    Math.sin(dLat / 2) ** 2 +
    Math.cos(rad(a.lat)) * Math.cos(rad(b.lat)) * Math.sin(dLon / 2) ** 2;
  return 2 * R * Math.asin(Math.sqrt(s));
}
