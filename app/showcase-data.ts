export interface Stop {
  time: string;
  title: string;
  /** 地图名称标签用的短名（去掉括号/说明，避免标签过长） */
  short: string;
  kind: Kind;
  detail: string;
  /** 显示用花费（"¥80" / "¥688/晚" / null=免费不显示） */
  cost: string | null;
  lat: number;
  lon: number;
  /** 该条目的选择依据（行程页可解释性；存为行程时写入 item.why） */
  why?: string;
  /** transit 条目以登机牌 / 车票票根呈现 */
  ticket?: {
    line: string;
    no: string;
    from: string;
    to: string;
    dep: string;
    arr: string;
    dur: string;
    seat: string;
    /** 交通方式：火车（默认）或航班 */
    mode?: "train" | "flight";
    /** 中转说明（如「经上海浦东」「赫尔辛基转机」） */
    via?: string;
  };
}

export interface ShowDay {
  day: number;
  date: string;
  dow: string;
  theme: string;
  tab: string;
  summary: string;
  stops: Stop[];
}

/** 一个完整 demo：卡片信息 + 逐日行程 + 关键信息 + 存库所需元数据 */
