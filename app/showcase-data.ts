  slug: string;
  name: string;
  en: string;
  /** 目的地所属地区，如「中国·江苏」「日本·关西」 */
  region: string;
  tagline: string;
  origin: string;
  transport: "train" | "flight";
  /** 交通一句话，如「高铁 20 分直达」「上海转机·关西空港」 */
  transportNote: string;
  /** 卡片/头部日期区间展示，如「07.10 – 07.12」 */
  dateLabel: string;
  /** 时长短语，如「江南三日」「关西红叶四日」 */
  durationLabel: string;
  partySize: number;
  /** 人均预算展示，如「¥1,200 / 人」 */
  budgetLabel: string;
  /** 存库时写入 trip 的总预算（约人均×人数） */
  budgetValue: number;
  /** 存库时写入 travel_style */
  style: string;
  /** 概览段落（demo 页 + 存库 overview） */
  overview: string;
  /** 成稿标题，如「无锡 → 苏州 · 江南三日」 */
  itineraryTitle: string;
  tiles: "amap" | "osm";
