/**
 * 地点名 → 地理编码查询变体（客户端高德 PlaceSearch 与服务端 OSM 编码共用）。
 *
 * LLM 生成的行程条目标题常是「活动描述」而非 POI 名（「夜游平江路」「拙政园深度游」
 * 「晚餐：观前街觅食」），直接拿去搜命中率低。按可信度产出变体：
 * 原名 → 去括号 → 剥活动动词前后缀 → 「·/：/，」分段（同样剥前后缀）→ 括号内容。
 * 纯场景词（「午餐」「自由活动」…）不产出——搜它们只会命中随机同名 POI，宁缺毋错。
 */

/** 活动动词前缀（「游览拙政园」→「拙政园」） */
const PREFIX_RE =
  /^(?:游览|参观|打卡|夜游|夜逛|漫步|漫游|闲逛|逛逛|逛|探访|探秘|走进|前往|抵达|入住|品尝|品味|体验|观赏|欣赏|拜访|寻味|觅食|骑行|徒步|夜宿|夜探)\s*/;

/** 活动性后缀（「平江路漫步」→「平江路」） */
const SUFFIX_RE =
  /\s*(?:一日游|半日游|深度游|深度体验|自由行|自由活动|漫步|散步|夜游|夜景|打卡|游玩|游览|参观|体验|之旅|之行|观景|看日出|看日落|看夜景|觅食|探店|美食探店|购物|逛街|休整|午后时光)$/;

/** 纯场景/时段词：单独出现时不值得搜（会命中随机同名 POI） */
const GENERIC = new Set([
  "早餐",
  "午餐",
  "晚餐",
  "早饭",
  "午饭",
  "晚饭",
  "夜宵",
  "下午茶",
  "咖啡",
  "酒店",
  "民宿",
  "入住",
  "退房",
  "休息",
  "休整",
  "集合",
  "出发",
  "返程",
  "抵达",
  "自由活动",
  "市区",
  "机场",
  "车站",
  "高铁站",
  "火车站",
  "景点",
  "美食",
  "小吃",
  "购物",
  "逛街",
  "打车",
  "地铁",
  "公交",
  "步行",
]);

/** 剥前后缀（剥空则保留原样，至少 2 字） */
function strip(s: string): string {
  let t = s.trim();
  const p = t.replace(PREFIX_RE, "").trim();
  if (p.length >= 2) t = p;
  const q = t.replace(SUFFIX_RE, "").trim();
  if (q.length >= 2) t = q;
  return t;
}

/**
 * 产出查询变体（首个恒为原名，后续按可信度排序，去重、封顶 cap 个）。
 * 例：「晚餐：观前街觅食」→ ["晚餐：观前街觅食", "观前街觅食", "观前街"]
 *     「游览拙政园（需预约）」→ ["游览拙政园（需预约）", "游览拙政园", "拙政园", "需预约"]
 * 注意：不产出「整串剥后缀但仍含分隔符」的中间形态（如「平江路 · 摇橹船」）——
 * 模糊搜索会把这类复合串匹配到随机 POI，且它必须排在「平江路」这种干净分段之后才安全。
 */
export function placeVariants(raw: string, cap = 6): string[] {
  const out: string[] = [];
  const push = (s: string) => {
    const t = s.trim().replace(/\s+/g, " ");
    if (t.length >= 2 && !GENERIC.has(t) && !out.includes(t)) out.push(t);
  };
  push(raw);
  const noParen = raw.replace(/[（(][^）)]*[）)]/g, " ").trim();
  push(noParen);
  for (const seg of noParen.split(/[·•—–，,、:：]/)) {
    push(seg);
    push(strip(seg));
  }
  for (const m of raw.matchAll(/[（(]([^）)]+)[）)]/g)) push(m[1]);
  return out.slice(0, cap);
}
